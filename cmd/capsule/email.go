package main

import (
	"encoding/json"
	"fmt"
	"os"
	"text/tabwriter"

	"github.com/spf13/cobra"
)

var emailCmd = &cobra.Command{
	Use:   "email",
	Short: "Manage AWS SES email domain integrations",
}

var emailSetupCmd = &cobra.Command{
	Use:   "setup",
	Short: "Setup an email sending domain with SES",
	RunE: func(cmd *cobra.Command, args []string) error {
		orgID, _ := cmd.Flags().GetString("org")
		if orgID == "" {
			orgID = cfg.OrgID
		}
		projectID, _ := cmd.Flags().GetString("project")
		domainName, _ := cmd.Flags().GetString("domain")

		if orgID == "" || projectID == "" || domainName == "" {
			return fmt.Errorf("--org, --project, and --domain are required")
		}

		body := map[string]string{"domain": domainName}
		var resp map[string]any
		path := fmt.Sprintf("/api/v1/orgs/%s/projects/%s/email/setup", orgID, projectID)
		if err := apiClient.Post(path, body, &resp); err != nil {
			return err
		}

		fmt.Println("┌─ Cost Estimate ──────────────────────────────┐")
		fmt.Println("│ Amazon SES Outbound Email                    │")
		fmt.Println("│                                              │")
		fmt.Println("│  Outbound:     $0.10 per 1,000 emails        │")
		fmt.Println("│  Data Transfer:$0.09/GB                      │")
		fmt.Println("│  ─────────────────────────────               │")
		fmt.Println("│  Estimated:    ~$1.00/10K emails             │")
		fmt.Println("└──────────────────────────────────────────────┘")
		fmt.Printf("Email Setup Initiated for domain %s (Status: %s)\n", resp["db_name"], resp["status"])
		fmt.Println("Next steps: DNS verification tokens are being generated by SES. Run 'capsule email stats' to verify.")
		return nil
	},
}

