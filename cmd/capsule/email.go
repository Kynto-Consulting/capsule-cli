var emailStatsCmd = &cobra.Command{
	Use:   "stats",
	Short: "View SES sending and bounce analytics",
	RunE: func(cmd *cobra.Command, args []string) error {
		orgID, _ := cmd.Flags().GetString("org")
		if orgID == "" {
			orgID = cfg.OrgID
		}
		projectID, _ := cmd.Flags().GetString("project")

		if orgID == "" || projectID == "" {
			return fmt.Errorf("--org and --project are required")
		}

		type emailStats struct {
			Sent       int `json:"sent"`
			Delivered  int `json:"delivered"`
			Bounces    int `json:"bounces"`
			Complaints int `json:"complaints"`
		}

		var stats emailStats
		path := fmt.Sprintf("/api/v1/orgs/%s/projects/%s/email/stats", orgID, projectID)
		if err := apiClient.Get(path, &stats); err != nil {
			return err
		}

		out, _ := cmd.Flags().GetString("output")
		if out == "json" {
			return json.NewEncoder(os.Stdout).Encode(stats)
		}

		w := tabwriter.NewWriter(os.Stdout, 0, 0, 2, ' ', 0)
		fmt.Fprintln(w, "SENT\tDELIVERED\tBOUNCES\tCOMPLAINTS\tDELIVERY RATE")
		rate := 0.0
		if stats.Sent > 0 {
			rate = float64(stats.Delivered) / float64(stats.Sent) * 100.0
		}
		fmt.Fprintf(w, "%d\t%d\t%d\t%d\t%.2f%%\n", stats.Sent, stats.Delivered, stats.Bounces, stats.Complaints, rate)
		return w.Flush()
	},
}

func init() {
	emailSetupCmd.Flags().String("org", "", "Org ID")
	emailSetupCmd.Flags().String("project", "", "Project ID")
	emailSetupCmd.Flags().String("domain", "", "Email domain to configure")

	emailTestCmd.Flags().String("org", "", "Org ID")
	emailTestCmd.Flags().String("project", "", "Project ID")
	emailTestCmd.Flags().String("to", "", "Recipient email address")

	emailStatsCmd.Flags().String("org", "", "Org ID")
	emailStatsCmd.Flags().String("project", "", "Project ID")

	emailCmd.AddCommand(emailSetupCmd, emailTestCmd, emailStatsCmd)
	rootCmd.AddCommand(emailCmd)
}
